import cv2
from matplotlib import pyplot as plt
import os
import numpy as np
from tensorflow.keras.preprocessing.image import load_img
from tensorflow.keras.preprocessing.image import img_to_array
from tensorflow.keras.models import load_model

filepath = '/content/model (1).h5'
model = load_model(filepath)
print(model)

print("Model Loaded Successfully")

Potato_plant = cv2.imread('/content/drive/MyDrive/Potato Disease Prediction /Potato Dataset/Test/Potato___Early_blight/034959c1-f1e8-4a79-a6d5-3c1d14efa2f3___RS_Early.B 7136.JPG')
test_image = cv2.resize(Potato_plant, (224,224)) # load image

test_image = img_to_array(test_image)/255 # convert image to np array and normalize
test_image = np.expand_dims(test_image, axis = 0) # change dimention 3D to 4D

result = model.predict(test_image) # predict diseased palnt or not

pred = np.argmax(result, axis=1)

print(pred)

if pred==0:
    print( "Potato___Early_blight Disease")
       
elif pred==1:
    print("Potato___Late_blight Disease")
        
elif pred==2:
    print("Potato___health and Freash")





